#include <stdio.h>

/**
 * main - Prints the name of the program
 * @argc: The number of command-line arguments (unused)
 * @argv: Array of command-line arguments
 *
 * Return: Always 0 (Success)
 */
int main(int argc __attribute__((unused)), char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);
}

